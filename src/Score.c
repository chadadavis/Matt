double RMSDScoreSimple(double rmsd, int length) {
	if (rmsd > 2.8) return 0;
	else {
		/* linear interpolation of table values. */
		double rmsd1000 = 1000*rmsd;
		int i = (int)rmsd1000;
		double r = rmsd1000 - i;
		return length * (RMSDTable[i] * (1-r) + r * RMSDTable[i+1]);
	}
}

double AngleScore(double angle) {
	if (angle<0.1) angle = 0.1;
	return -log((1 - cos(angle))/2);
}

double DisplacementScore(double displacement) {
	return (4-displacement);
}

